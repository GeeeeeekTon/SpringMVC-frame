package com.dongao.core.mycomponent.templateSource;

/**
 * 移动端推广
 * @author zhangzhen
 */
public class MobileSpreadSource implements ItemplateSource{
	/**超链接*/
	private String href;
	/**图片地址*/
	private String url;
	public String getHref() {
		return href;
	}
	public void setHref(String href) {
		this.href = href;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
}
