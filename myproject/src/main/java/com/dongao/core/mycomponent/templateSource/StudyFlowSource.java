package com.dongao.core.mycomponent.templateSource;

/**
 * 学习流程
 * @author zhangzhen
 *
 */
public class StudyFlowSource implements ItemplateSource{
	/**图片地址*/
	private String url;
	/**图片地址对应超级链接*/
	private String href;
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getHref() {
		return href;
	}
	public void setHref(String href) {
		this.href = href;
	}
}
