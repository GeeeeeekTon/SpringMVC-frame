package com.dongao.core.mycomponent.templateSource;

/**
 * 个性logo配置
 * @author zhangzhen
 */
public class LogoSource implements ItemplateSource{
	/**logo图片地址* */
	private String url;
	/**超链接*/
	private String href;
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getHref() {
		return href;
	}
	public void setHref(String href) {
		this.href = href;
	}
	
}
