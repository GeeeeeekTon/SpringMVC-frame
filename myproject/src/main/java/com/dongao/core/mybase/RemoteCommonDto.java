package com.dongao.core.mybase;

import java.io.Serializable;

/**
 * @author zhangzhen
 *
 */
@SuppressWarnings("serial")
public class RemoteCommonDto implements Serializable {
	private String id;
	private String name;
	
	public RemoteCommonDto(String id, String name) {
		this.id = id;
		this.name = name;
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
