package com.dongao.core.mybase;

import java.io.Serializable;
/**
 * 试题
 * @author zhangzhen
 *
 */
@SuppressWarnings("serial")
public class ExamQuestionDto implements Serializable{
    private Long id;// 自增主键

    private Integer status;// 状态 1:新建；2：激活；3：隐藏

    private String tag;// 题目关键字：知识点，如果是多个，用，分隔

    private String subject;// 主题

    private Integer choiceType;// 试题类型。1：单项选择题；2：多项选择题；3：判断题；
                               // 4：简答题；5：计算题；6：综合题；7：其它；8：计算分析题；9：案例分析题；10：操作题；11：不定向选择题；12：实操题;13:计算题（单选模式）；14：综合分析题（不定项模式）

    private Long examId;// 考试ID

    private String courseId;// 课程ID

    private Long chapterId;// 章节ID，对应 section

    private String creator;// 创建人

    private Integer difficultyRating;// 难度等级。1：易；2：中；3：难。

    private Long reclassId;// 继续教育讲座id 试题所绑定的继教班次

    private String brief;// 简报

    private Long groupId;// 题帽

    private Integer category;// 试题类型 0：普通试题；1：继续教育弹题专用；

    private Double scoreValue;// 试题分值

    private Integer useSys;// 使用系统 试题适用于什么系统 1：考前；2：机考；3：通用；4：四大所


    private String selfType;// 自定义试题类型

    private String realOperAddr;// 实操视频地址

    private String bigSubject;// 大主题 题目内容 题干

    private String smallSubject;// 小主题 小标题
                                // 题对应信息描述，例如：2007年初级会计职称考试&gt;2007年初级会计实务&gt;第3章

    private String realAnswer;// 正确答案

    private String quizAnalyze;// 试题解析

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public String getTag() {
		return tag;
	}

	public void setTag(String tag) {
		this.tag = tag;
	}

	public String getSubject() {
		return subject;
	}

	public void setSubject(String subject) {
		this.subject = subject;
	}

	public Integer getChoiceType() {
		return choiceType;
	}

	public void setChoiceType(Integer choiceType) {
		this.choiceType = choiceType;
	}

	public Long getExamId() {
		return examId;
	}

	public void setExamId(Long examId) {
		this.examId = examId;
	}

	public String getCourseId() {
		return courseId;
	}

	public void setCourseId(String courseId) {
		this.courseId = courseId;
	}

	public Long getChapterId() {
		return chapterId;
	}

	public void setChapterId(Long chapterId) {
		this.chapterId = chapterId;
	}

	public String getCreator() {
		return creator;
	}

	public void setCreator(String creator) {
		this.creator = creator;
	}

	public Integer getDifficultyRating() {
		return difficultyRating;
	}

	public void setDifficultyRating(Integer difficultyRating) {
		this.difficultyRating = difficultyRating;
	}

	public Long getReclassId() {
		return reclassId;
	}

	public void setReclassId(Long reclassId) {
		this.reclassId = reclassId;
	}

	public String getBrief() {
		return brief;
	}

	public void setBrief(String brief) {
		this.brief = brief;
	}

	public Long getGroupId() {
		return groupId;
	}

	public void setGroupId(Long groupId) {
		this.groupId = groupId;
	}

	public Integer getCategory() {
		return category;
	}

	public void setCategory(Integer category) {
		this.category = category;
	}

	public Double getScoreValue() {
		return scoreValue;
	}

	public void setScoreValue(Double scoreValue) {
		this.scoreValue = scoreValue;
	}

	public Integer getUseSys() {
		return useSys;
	}

	public void setUseSys(Integer useSys) {
		this.useSys = useSys;
	}

	public String getSelfType() {
		return selfType;
	}

	public void setSelfType(String selfType) {
		this.selfType = selfType;
	}

	public String getRealOperAddr() {
		return realOperAddr;
	}

	public void setRealOperAddr(String realOperAddr) {
		this.realOperAddr = realOperAddr;
	}

	public String getBigSubject() {
		return bigSubject;
	}

	public void setBigSubject(String bigSubject) {
		this.bigSubject = bigSubject;
	}

	public String getSmallSubject() {
		return smallSubject;
	}

	public void setSmallSubject(String smallSubject) {
		this.smallSubject = smallSubject;
	}

	public String getRealAnswer() {
		return realAnswer;
	}

	public void setRealAnswer(String realAnswer) {
		this.realAnswer = realAnswer;
	}

	public String getQuizAnalyze() {
		return quizAnalyze;
	}

	public void setQuizAnalyze(String quizAnalyze) {
		this.quizAnalyze = quizAnalyze;
	}
    


}
